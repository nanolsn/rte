

namespace OpenGLEngine.Engine
{
    struct Vertex4D
    {
        public readonly float X;
        public readonly float Y;
        public readonly float Z;
        public readonly float U;

        public Vertex4D(float x, float y, float z, float u)
        {
            X = x;
            Y = y;
            Z = z;
            U = u;
        }
    }
}
